pub const FONT_W: usize = 8;
pub const FONT_H: usize = 16;

/// Draw a single character at (x, y) with given color
pub fn draw_char(x: usize, y: usize, ch: char, color: u32) {
    let idx = if (ch as u32) >= 32 && (ch as u32) <= 126 {
        (ch as usize) - 32
    } else {
        0
    };
    let glyph = &FONT_DATA[idx];
    for row in 0..16 {
        let bits = glyph[row];
        for col in 0..8 {
            if bits & (0x80 >> col) != 0 {
                put_pixel(x + col, y + row, color);
            }
        }
    }
}

/// Draw a string at (x, y) with given color
pub fn draw_str(x: usize, y: usize, s: &str, color: u32) {
    let mut cx = x;
    for ch in s.chars() {
        if ch == '\n' {
            cx = x;
            continue;
        }
        draw_char(cx, y, ch, color);
        cx += FONT_W;
    }
}

/// Measure string width in pixels
pub fn str_width(s: &str) -> usize {
    s.len() * FONT_W
}

/// Fill the entire framebuffer with a solid color
pub fn clear(color: u32) {
    fill_rect(0, 0, unsafe { FB_W }, unsafe { FB_H }, color);
}
