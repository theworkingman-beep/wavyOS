//! Framebuffer console with alpha compositing, text rendering, and drawing primitives
use core::slice;
use common::FramebufferInfo;

static mut FB_PTR: *mut u8 = core::ptr::null_mut();
static mut FB_W: usize = 0;
static mut FB_H: usize = 0;
static mut FB_PITCH: usize = 0;
static mut FB_BPP: usize = 0;

pub fn init(info: &FramebufferInfo) {
    unsafe {
        FB_PTR = info.addr as *mut u8;
        FB_W = info.width as usize;
        FB_H = info.height as usize;
        FB_PITCH = info.pitch as usize;
        FB_BPP = info.bpp as usize;
    }
}

pub unsafe fn fb_width() -> usize {
    FB_W
}

pub unsafe fn fb_height() -> usize {
    FB_H
}

pub unsafe fn raw_ptr() -> *mut u8 {
    FB_PTR
}

pub struct FramebufferRef {
    pub ptr: *mut u8,
    pub info: FramebufferInfo,
}

pub fn get_framebuffer() -> FramebufferRef {
    unsafe {
        FramebufferRef {
            ptr: FB_PTR,
            info: FramebufferInfo {
                addr: FB_PTR as u64,
                width: FB_W as u32,
                height: FB_H as u32,
                pitch: FB_PITCH as u32,
                bpp: FB_BPP as u8,
            },
        }
    }
}

pub fn set_pixel(x: usize, y: usize, color: u32) {
    put_pixel(x, y, color);
}

/// Write a 32-bit color pixel at (x, y) with alpha blending
pub fn put_pixel(x: usize, y: usize, color: u32) {
    unsafe {
        if FB_PTR.is_null() || FB_BPP == 0 || x >= FB_W || y >= FB_H {
            return;
        }
        let off = y * FB_PITCH + x * 4;
        let buf = slice::from_raw_parts_mut(FB_PTR.add(off), 4);
        buf[0] = color as u8;         // B
        buf[1] = (color >> 8) as u8;  // G
        buf[2] = (color >> 16) as u8; // R
        buf[3] = (color >> 24) as u8; // A
    }
}

/// Blend a pixel with alpha (0-255) over existing framebuffer content
pub fn put_pixel_alpha(x: usize, y: usize, color: u32) {
    unsafe {
        if FB_PTR.is_null() || FB_BPP == 0 || x >= FB_W || y >= FB_H {
            return;
        }
        let alpha = ((color >> 24) & 0xFF) as u32;
        if alpha == 0 {
            return;
        }
        let off = y * FB_PITCH + x * 4;
        let buf = slice::from_raw_parts_mut(FB_PTR.add(off), 4);
        let bg_b = buf[0] as u32;
        let bg_g = buf[1] as u32;
        let bg_r = buf[2] as u32;
        let fg_r = (color >> 16) & 0xFF;
        let fg_g = (color >> 8) & 0xFF;
        let fg_b = color & 0xFF;
        let inv = 255 - alpha;
        buf[0] = ((fg_b * alpha + bg_b * inv) / 255) as u8;
        buf[1] = ((fg_g * alpha + bg_g * inv) / 255) as u8;
        buf[2] = ((fg_r * alpha + bg_r * inv) / 255) as u8;
        buf[3] = 0xFF;
    }
}

/// Fill rectangle with solid color
pub fn fill_rect(x: usize, y: usize, w: usize, h: usize, color: u32) {
    unsafe {
        let max_y = (y + h).min(FB_H);
        let max_x = (x + w).min(FB_W);
        for cy in y..max_y {
            for cx in x..max_x {
                put_pixel(cx, cy, color);
            }
        }
    }
}

/// Fill rectangle with alpha blending
pub fn fill_rect_alpha(x: usize, y: usize, w: usize, h: usize, color: u32) {
    unsafe {
        let max_y = (y + h).min(FB_H);
        let max_x = (x + w).min(FB_W);
        for cy in y..max_y {
            for cx in x..max_x {
                put_pixel_alpha(cx, cy, color);
            }
        }
    }
}

/// Draw a filled rectangle with horizontal gradient from color_left to color_right
pub fn fill_rect_h_gradient(x: usize, y: usize, w: usize, h: usize, color_left: u32, color_right: u32) {
    let lr = (color_left >> 16) & 0xFF;
    let lg = (color_left >> 8) & 0xFF;
    let lb = color_left & 0xFF;
    let rr = (color_right >> 16) & 0xFF;
    let rg = (color_right >> 8) & 0xFF;
    let rb = color_right & 0xFF;
    unsafe {
        let max_x = (x + w).min(FB_W);
        let max_y = (y + h).min(FB_H);
        for cx in x..max_x {
            let t = if w > 1 { (cx - x) as u32 / (w as u32 - 1) } else { 0 };
            let r = lr as i32 + ((rr as i32 - lr as i32) * t as i32 / 255);
            let g = lg as i32 + ((rg as i32 - lg as i32) * t as i32 / 255);
            let b = lb as i32 + ((rb as i32 - lb as i32) * t as i32 / 255);
            let color = 0xFF000000u32 | ((r as u32 & 0xFF) << 16) | ((g as u32 & 0xFF) << 8) | (b as u32 & 0xFF);
            for cy in y..max_y {
                put_pixel(cx, cy, color);
            }
        }
    }
}

/// Draw a filled rectangle with vertical gradient from color_top to color_bottom
pub fn fill_rect_v_gradient(x: usize, y: usize, w: usize, h: usize, color_top: u32, color_bottom: u32) {
    let tr = (color_top >> 16) & 0xFF;
    let tg = (color_top >> 8) & 0xFF;
    let tb = color_top & 0xFF;
    let br = (color_bottom >> 16) & 0xFF;
    let bg = (color_bottom >> 8) & 0xFF;
    let bb = color_bottom & 0xFF;
    unsafe {
        let max_y = (y + h).min(FB_H);
        let max_x = (x + w).min(FB_W);
        for cy in y..max_y {
            let t = if h > 1 { (cy - y) as u32 / (h as u32 - 1) } else { 0 };
            let r = tr as i32 + ((br as i32 - tr as i32) * t as i32 / 255);
            let g = tg as i32 + ((bg as i32 - tg as i32) * t as i32 / 255);
            let b = tb as i32 + ((bb as i32 - tb as i32) * t as i32 / 255);
            let color = 0xFF000000u32 | ((r as u32 & 0xFF) << 16) | ((g as u32 & 0xFF) << 8) | (b as u32 & 0xFF);
            for cx in x..max_x {
                put_pixel(cx, cy, color);
            }
        }
    }
}

/// Draw a filled rounded rectangle
pub fn fill_rounded_rect(x: usize, y: usize, w: usize, h: usize, radius: usize, color: u32) {
    // Main body (rectangle without corners)
    fill_rect(x + radius, y, w - 2 * radius, h, color);
    fill_rect(x, y + radius, w, h - 2 * radius, color);
    // Corners (approximated with filled circles)
    fill_circle_approx(x + radius, y + radius, radius, color);
    fill_circle_approx(x + w - 1 - radius, y + radius, radius, color);
    fill_circle_approx(x + radius, y + h - 1 - radius, radius, color);
    fill_circle_approx(x + w - 1 - radius, y + h - 1 - radius, radius, color);
}

/// Approximate filled circle using bounding box check
pub fn fill_circle_approx(cx: usize, cy: usize, r: usize, color: u32) {
    let r2 = (r * r) as i32;
    for dy in 0..=(r as i32) {
        for dx in 0..=(r as i32) {
            if dx * dx + dy * dy <= r2 {
                put_pixel((cx as i32 + dx) as usize, (cy as i32 + dy) as usize, color);
                put_pixel((cx as i32 - dx) as usize, (cy as i32 + dy) as usize, color);
                put_pixel((cx as i32 + dx) as usize, (cy as i32 - dy) as usize, color);
                put_pixel((cx as i32 - dx) as usize, (cy as i32 - dy) as usize, color);
            }
        }
    }
}

/// Draw a horizontal line
pub fn draw_hline(x: usize, y: usize, w: usize, color: u32) {
    fill_rect(x, y, w, 1, color);
}

/// Draw a vertical line
pub fn draw_vline(x: usize, y: usize, h: usize, color: u32) {
    fill_rect(x, y, 1, h, color);
}

/// Draw a border around a rectangle
pub fn draw_border(x: usize, y: usize, w: usize, h: usize, thickness: usize, color: u32) {
    fill_rect(x, y, w, thickness, color);
    fill_rect(x, y + h - thickness, w, thickness, color);
    fill_rect(x, y, thickness, h, color);
    fill_rect(x + w - thickness, y, thickness, h, color);
}

/// Draw a shadow (soft offset rectangle below and right)
pub fn draw_shadow(x: usize, y: usize, w: usize, h: usize, offset: usize) {
    // Right shadow
    fill_rect_alpha(x + w, y + offset, offset, h - offset, 0x00000080);
    // Bottom shadow
    fill_rect_alpha(x + offset, y + h, w - offset, offset, 0x00000080);
    // Corner shadow
    fill_rect_alpha(x + w, y + h, offset, offset, 0x00000060);
}

